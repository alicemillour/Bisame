<h2>Charte d'Admission</h2>

<p>
<b class="ostrich">BISAME</b> est une plate-forme gratuite créé dans le cadre d'un projet de recherche
par 
<a href="https://alicemillour.github.io/" target="_blank">Alice Millour</a>,
<a href="http://www.schplaf.org/kf/" target="_blank">Karën Fort</a>
dans le cadre de la thèse d'Alice Millour réalisée à l' 
<a href="http://www.paris-sorbonne.fr/" target="_blank">Sorbonne Universités</a>
et conjointement avec 
<a href="http://www.loria.fr/~guillaum" target="_blank">Bruno Guillaume</a>
(<a href="http://www.loria.fr" target="_blank">LORIA</a>), 
<a href="http://www.loria.fr/~guillaum" target="_blank">Delphine Berhnard</a>
(<a href="http://www.loria.fr" target="_blank">LiLPa</a>), et 
<a href="http://www.loria.fr/~guillaum" target="_blank">André Thibault</a>
(<a href="http://www.paris-sorbonne.fr/" target="_blank">Sorbonne Universités</a>).
dans le cadre du projet PLURAL (Production LUdique de Ressources Annotées pour les Langues de France), 
financé par la <a href="http://www.culturecommunication.gouv.fr/Thematiques/Langue-francaise-et-langues-de-France" target="_blank">DGLFLF</a>.
Une partie des développements a été réalisée par Nicolas Lefebvre au LORIA.

L'objet de ce projet est la construction de corpus annotés pour des applications de traitement automatique de la langue (TAL).
Ces données sont le produit de l'activité des participants de <b class="ostrich">BISAME</b>.
</p>
<p>
Les données sont {!! link_to('corpora','accessibles',['target'=>'_blank']) !!} sous des licences libres variées (fonction de la licence de la ressource d'origine), qui sont précisées dans chaque ressource.
Le code source de <b class="ostrich">BISAME</b> est librement accessible sur <a href="https://github.com/alicemillour/Bisame" target="_blank">github</a> sous licence <a href="http://www.cecill.info/" target="_blank">CeCILL v2.1</a>.
</p>
<p>
La présente charte a pour but de permettre aux participants de participer dans l'environnement le plus agréable possible, s'ils respectent les quelques règles établies ci-dessous.
</p>
<p>
Tout manquement ou tout abus fera l'objet de sanctions pouvant aller jusqu'au banissement définitif du joueur.
 De manière générale, il est attendu de la part des participants un comportement fair-play visant à respecter l'amusement de tous.
</p>
<h2>
(1) Inscription
</h2>
<p>
La participation à la plateforme <b class="ostrich">BISAME</b> est entièrement gratuite et l'inscription est ouverte à tous les internautes disposant d'un accès à Internet régulier et éventuellement d'une adresse mail personnelle et active (ce dernier point n'étant pas obligatoire).
Des mails pourront être envoyés au participant en fonctions des évolutions de la plate-forme.
Les corpus proposés dans <b class="ostrich">BISAME</b> peuvent contenir des textes ne convenant pas à des personnes de moins de 16 ans. 
</p>
<h2>
(2) Données Personnelles
</h2>
<p>
Les informations recueillies font l’objet d’un traitement informatique destiné à assurer le bon fonctionnement de la plate-forme.
Certaines informations enregistrées dans notre base de données sont personnelles (mot de passe, adresse mail, etc.), d'autres sont amenées à être éventuellement fournies à d'autres personnes (réponses proposées par le joueur lors d'une partie, classement, etc.) dans le cadre du projet.
Aucune diffusion des Données Personnelles ne sera faite à aucun titre que ce soit en dehors des modalités prévues par la plate-forme (informations affichées dans le Classement, etc.).
</p>
<p>
Les destinataires des données sont&nbsp;: tous les membres de l'équipe du projet, sans restriction.
L'équipe du projet se réserve le droit d'exploiter à des fins de recherches les données langagières récoltées lors d'une session (une fois celle-ci achevée).
En aucun cas, ces données ne contiennent d'informations personnelles permettant de retrouver leur(s) auteur(s).
</p>
<p>
Le joueur peut à tout moment supprimer son compte ainsi que la totalité des informations le concernant dans la base de données.
Cependant, les actions liées à ce participant et qui sont utiles pour le construction de la ressource linguistiques seront conservées.
</p>
<p>
Conformément à la loi «&nbsp;informatique et libertés&nbsp;» du 6 janvier 1978 modifiée en 2004,
vous bénéficiez d’un droit d’accès et de rectification aux informations qui vous concernent, que vous pouvez exercer en vous adressant à&nbsp;:
</p>

<p>
<a href="mailto:alice.millour@paris-sorbonne.fr">Alice Millour</a>, STIH Université Paris-Sorbonne - Maison de la recherche - 28, rue Serpente 75006 Paris, France
</p>

<h2>
(3) Saisies
</h2>

<p>
Certains termes, textes ou certaines questions proposés dans <b class="ostrich">BISAME</b> peuvent être considérés comme profanes, vulgaires ou offensants par certains joueurs.
L'objet de <b class="ostrich">BISAME</b> étant de constituer des ressources langagières couvrant l'ensemble des faits ou opinions possibles, de telles informations ne seront a priori pas retirées du jeu.
Toutefois, les joueurs sont encouragés à signaler tout contenu inapproprié.
</p>
<h2>
(4) Vente de comptes
</h2>
<p>
La vente pour de l'argent ou des biens matériels d'un compte et/ou d'éléments du jeu est formellement interdite.
</p>
<h2>
(5) Automatisation
</h2>
<p>
Un compte doit être contrôlé par un participant&nbsp;: il est formellement interdit d'automatiser les actions par quelque moyen que ce soit (script, programme, site Web, etc.).
</p>
<h2>
(6) Bogues
</h2>
<p>
<b class="ostrich">BISAME</b> n'est pas à l'abri de bogues et incohérences.
Chaque participant est tenu de rapporter (par mail ou <i> via </i> le formulaire de contact) au plus vite tout problème décelé, et toute utilisation abusive ou volontaire d'un bogue ou d'une faille se verra sanctionnée et pourra se solder par la suppression du compte du participant concerné.
Des compensations pourront intervenir de la part des administrateurs, mais ils en seront seuls juges.
</p>
<h2>
(7) Information relative aux "cookies" de navigation
</h2>
<p>
Pour le bon fonctionnement du jeu, nous souhaitons implanter un cookie dans votre ordinateur.
Un cookie ne nous permet pas de vous identifier ; en revanche, il enregistre des informations relatives à la navigation de votre ordinateur sur notre site (les pages que vous avez consultées, la date et l'heure de la consultation, etc.) que nous pourrons lire lors de vos visites ultérieures. La durée de conservation de ces informations dans votre ordinateur est de plusieurs mois.
Vous pouvez vous opposer à l'enregistrement de cookies, toutefois <b class="ostrich">BISAME</b> ne pourra alors pas fonctionner normalement.
Nous vous informons que vous pouvez vous opposer à l'enregistrement de cookies en configurant votre navigateur de la manière suivante :

Pour Microsoft Internet Explorer 6.0 : 1. choisissez le menu "Outils" (ou "Tools"), puis "Options Internet" (ou "Internet Options"). 2. cliquez sur l'onglet "Confidentialité" (ou "Confidentiality") 3. sélectionnez le niveau souhaité à l'aide du curseur.

Pour Firefox : 1. choisissez le menu "Outils">"Options" ; 2. cliquez sur l'option "Vie privée"; 3. rubrique "Cookies".
Pour Opéra 6.0 et au-delà : 1. choisissez le menu "Fichier">"Préférences" ; 2. Vie Privée.
</p>
<h2>
(8) Addiction
</h2>
<p>
Si vous observez des modifications notables dans votre vie quotidienne: syndromes de manque, perte de contrôle par rapport aux jeux, conflits avec vos proches, vous souffrez peut-être d'addiction aux jeux. Nous vous conseillons dans ce cas de contacter \emph{Joueurs Info Service} (http://www.joueurs-info-service.fr/).
</p>

<h2>
(9) Charte d'admission
</h2>
<p>
La présente charte doit être lue et acceptée dans son intégralité pour pouvoir participer à <b class="ostrich">BISAME</b>.
Elle est susceptible d'être modifiée par les gestionnaires de la plate-forme.
Le cas échéant, il sera demandé aux participants d'accepter explicitement les modifications apportées pour pouvoir continuer à participer.

