Malgré ses 450 000 locuteurs, le <b class="ostrich">Creole Guadeloupeen</b> fait partie des langues peu dotées dans le domaine du traitement automatique du langage. 

<br>Cela s’explique principalement par la faible quantité de données électroniques accessibles en ligne. <br> De plus quand celles-ci sont accessibles, le plus souvent elles ne sont pas annotées en catégorie grammaticale.
 Nous avons donc décidé de développer cette plate-forme afin de recueillir des données linguistiques annotées qui permettront de développer des outils informatiques pour le Créole Guadeloupéen.  

<br>Créolophones, natifs ou amateurs, nous avons donc besoin de vous ! 
<b>  <br>Avec <b class="ostrich">KRIK</b>, participez à la création du premier corpus annoté en catégories grammaticales du Créole Guadeloupéen. </b>
 
