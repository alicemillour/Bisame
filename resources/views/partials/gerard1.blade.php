KIND HASCH GEDULD 

Wenn dinna Màma àlt worra ìsch
Un dü salbscht nìmma gànz jung bìsch,
Do gìt’s àls a manka schwara Stund,
Waga dr Màma Ihrem Gedachtnis-Schwund.

Drno brücht Sie dìch gànz notwandig,
Do müasch Ihra bisteh fàscht standig,
Denn Sie hàt dìch z’froga un z’froga !
Blib geduldig, àui wenn’s di düat ploga.

Un wenn Sie s’glicha frogt, ìmmer wìder,
Beklàg di nìt Kìnd, wìr nìt bìtter,
Denn as kummt dia Zitt, dank güat drà
Wu Sie dìch Nix meh froga kà !

